public class Main {
    public static void main(String[] args) {

        int sumatoria = 0;
        int s = 1;

        while (s <= 10) {
            sumatoria += s;
            s++;
            System.out.println("Sumatoria de los numeros es :" + sumatoria);
        }

    }
}